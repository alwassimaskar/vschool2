$(".box").hover(function(){
    $(this).css("background-color","Blue");
})


$(".box").mousedown(function(){
    $(this).css("background-color","red");
})


$(".box").dblclick(function(){
    $(this).css("background-color","Green");
})


$(".box").mouseup(function(){
    $(this).css("background-color","orange");
})